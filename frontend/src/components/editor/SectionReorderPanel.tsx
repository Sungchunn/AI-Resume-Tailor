interface SortableItemProps {
  id: string;
  index: number;
}

function SortableItem({ id, index }: SortableItemProps) {
  const {
    attributes,
    listeners,
    setNodeRef,
    transform,
    transition,
    isDragging,
  } = useSortable({ id });

  const style = {
    transform: CSS.Transform.toString(transform),
    transition,
  };

  return (
    <div
      ref={setNodeRef}
      style={style}
      className={`flex items-center gap-3 p-3 bg-white border rounded-lg ${
        isDragging ? "shadow-lg border-primary-300 z-10" : "border-gray-200"
      }`}
      {...attributes}
    >
      <button
        className="cursor-grab active:cursor-grabbing text-gray-400 hover:text-gray-600"
        {...listeners}
      >
        <svg className="w-5 h-5" fill="none" viewBox="0 0 24 24" stroke="currentColor">
          <path strokeLinecap="round" strokeLinejoin="round" strokeWidth={2} d="M4 8h16M4 16h16" />
        </svg>
      </button>
      <span className="flex-shrink-0 text-gray-500">
        {SECTION_ICONS[id] || SECTION_ICONS.summary}
      </span>
      <span className="flex-1 text-sm font-medium text-gray-700">
        {SECTION_LABELS[id] || id}
      </span>
      <span className="text-xs text-gray-400">{index + 1}</span>
    </div>
  );
}

export function SectionReorderPanel({
  sections,
  onChange,
  onReset,
}: SectionReorderPanelProps) {
  const sensors = useSensors(
    useSensor(PointerSensor),
    useSensor(KeyboardSensor, {
      coordinateGetter: sortableKeyboardCoordinates,
    })
  );

  const handleDragEnd = (event: DragEndEvent) => {
    const { active, over } = event;

    if (over && active.id !== over.id) {
      const oldIndex = sections.indexOf(active.id as string);
      const newIndex = sections.indexOf(over.id as string);
      onChange(arrayMove(sections, oldIndex, newIndex));
    }
  };

  return (
    <div className="h-full overflow-y-auto p-4">
      <div className="flex items-center justify-between mb-4">
        <h3 className="text-sm font-semibold text-gray-900">Section Order</h3>
        <button
          onClick={onReset}
          className="text-xs text-gray-500 hover:text-gray-700"
        >
          Reset Order
        </button>
      </div>

      <p className="text-xs text-gray-500 mb-4">
        Drag and drop to reorder sections in your resume.
      </p>

      <DndContext
        sensors={sensors}
        collisionDetection={closestCenter}
        onDragEnd={handleDragEnd}
      >
        <SortableContext items={sections} strategy={verticalListSortingStrategy}>
          <div className="space-y-2">
            {sections.map((section, index) => (
              <SortableItem key={section} id={section} index={index} />
            ))}
          </div>
        </SortableContext>
      </DndContext>
    </div>
  );
}
